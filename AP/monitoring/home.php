<h2>Intensive Cardiac Care Unit</h2>


<p>Web monitoring detak jantung pasien intensive cardiac care unit (ICCU) merupakan fasilitas yang disediakan agar membantu dokter dan perawat untuk melakukan monitoring detak jantung pasien yang berada di ruang ICCU. Web ini dapat diakses dimanapun dan kapanpun oleh dokter dan perawat sehingga mempercepat penanganan.</p>

	